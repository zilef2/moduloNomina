"use strict";
const calendar = require("./app.js");

/**
 * @constant string - restablece el color de la consola
 * @default
 */
const ANSI_RESET = "\u001B[0m";
/**
 * @constant string - restablece el color verde al texto de la consola
 * @default
 */
const ANSI_GREEN = "\u001B[32m";
/**
 * @constant string - establece el color amarillo al texto de la consola
 * @default
 */
const ANSI_YELLOW = "\u001B[33m";

console.log(ANSI_YELLOW + "Días festivos litúrgico en color amarillo");
console.log(ANSI_GREEN + "Días festivos fijos en color verde" + ANSI_RESET + "\n");

let year = 2018;

let holidays = calendar.getHolidaysByYear(year);
holidays.forEach(element => {
	if (element.static) {
		console.log(element.date + " - " + element.name);
	} else {
		console.log(element.date + " - " + element.name);
	}
});

holidays = calendar.getHolidaysByYearInterval(year, 2026);
holidays.forEach(obj => {
	console.log("Año: " + obj.year);
	obj.holidays.forEach(element => {
		if (element.static) {
			console.log(element.date + " - " + element.name);
		} else {
			console.log(element.date + " - " + element.name);
		}
	});
});

